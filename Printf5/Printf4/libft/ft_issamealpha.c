#include "libft.h"

int	ft_issamealpha(char c, char d)
{
	if ((!(ft_isalpha(c) && ft_isalpha(d)) && (c != '%' || d != '%')))
		return (0);
	if (!(c == d || c - 32 == d || c + 32 == d))
		return (0);
	return (1);
}
