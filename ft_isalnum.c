#include "libft.h"

int	ft_isalnum(int c)
{
	unsigned char nb;

	nb = (unsigned char)c;
	if ((nb >= '0') && (nb <= '9'))
		return (1);
	if ((nb >= 65) && (nb <= 122))
		if ((nb >= 97) || (nb <= 90))
			return (1);
	return (0);
}
