#include "libft.h"

int	ft_strnequ(const char *s1, const char *s2, size_t n)
{
	int	i;

	i = 0;
	if ((n == 0)
	|| ((s1[i] == s2[i]) && (s1[i + 1] == '\0') && (s2[i + 1] == '\0')))
		return (1);
	if ((s1[0] == '\0') && (s2[0] == '\0'))
		return (0);
	if ((s1[i] != '\0') && (s2[i] != '\0') && (n > 0))
	{
		i = 0;
		while ((n > 0) && ((s2[i] != '\0') || (s1[i] != '\0')))
		{
			if ((s1[i] != s2[i]))
				return (0);
			i++;
			n--;
		}
		return (1);
	}
	return (0);
}
